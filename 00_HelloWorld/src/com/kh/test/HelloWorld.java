package com.kh.test;
//클래스와 패키지 사이에 임포트가 있다?

public class HelloWorld {
	//이게 바로 주석이다~~
	/* 메인메소드? 자바 애플리케이션, 자바 프로그램이 시작되는 시작점(entry point)
	따라서 하나의 java 애플리케이션에는 1개 이상의 main 메서드가 포함된 클래스가 존재해야한다.
	 */
	public static void main(String[] args) {
		//출력문 아래 있는 콘솔창 ("문자열") 들어간 값을 출력해준다.
		System.out.println("Hello World!");
	}
}
